def adddn(x,y):
	""" Addition Function

	Addition Function takes atleast of two inputs thereby gives the sum of them.

	Args:
		x (int): First Argument to pass as a parameter
		y (int): Second Argument to pass as a parameter

	Returns:
		z (int): x(int) + y(int)

	"""
	z = x+y
	return z

def mulln(x,y):
	""" Multiplication Function.
	
	Multiplication Function takes atleast of two inputs thereby gives the product of them.

	Args:
		x (int): First Argument to pass as a parameter
		y (int): Second Argument to pass as a parameter

	Returns:
		z (int): x(int) * y(int)

	"""
	z = x+y
	return z
